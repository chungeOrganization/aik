package com.aik.model;

import java.util.Date;

public class AikAnswer {
    private Integer id;

    private Integer orderId;

    private Integer doctorId;

    private String answer;

    private Integer fromQuestionId;

    private Byte type;

    private Date createDate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getOrderId() {
        return orderId;
    }

    public void setOrderId(Integer orderId) {
        this.orderId = orderId;
    }

    public Integer getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(Integer doctorId) {
        this.doctorId = doctorId;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer == null ? null : answer.trim();
    }

    public Integer getFromQuestionId() {
        return fromQuestionId;
    }

    public void setFromQuestionId(Integer fromQuestionId) {
        this.fromQuestionId = fromQuestionId;
    }

    public Byte getType() {
        return type;
    }

    public void setType(Byte type) {
        this.type = type;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
}